package AgropetSapeca;

public class produtoVenda {
	
	private String id_solicitacao;
	private String id_produto;
	private String id_venda;
	private int quantidade;
	
	
	public String getId_solicitacao() {
		return id_solicitacao;
	}
	public void setId_solicitacao(String id_solicitacao) {
		this.id_solicitacao = id_solicitacao;
	}
	public String getId_produto() {
		return id_produto;
	}
	public void setId_produto(String id_produto) {
		this.id_produto = id_produto;
	}
	public String getId_venda() {
		return id_venda;
	}
	public void setId_venda(String id_venda) {
		this.id_venda = id_venda;
	}
	public int getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	

}
